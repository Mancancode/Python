import random

#prints the random numbers between the elements
print(random.randrange(40))
print(random.randrange(100))

print(random.randrange(0,50,10))
print(random.randrange(1,100,5))
print(random.randrange(1,100,20))

#printing the random integers
print(random.randint(0,50))
print(random.randint(10,100))

